public class SanPhamDonHang {
    private int maSP;
    private String name;
    private String oldPrice;
    private String newPrice;
    private int giamGia;
    private String thuongHieu;
    private String image;
    private int soluongDH;
    private String priceDH;

    public SanPhamDonHang() {
    }

    public int getMaSP() {
        return maSP;
    }

    public void setMaSP(int maSP) {
        this.maSP = maSP;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOldPrice() {
        return oldPrice;
    }

    public void setOldPrice(String oldPrice) {
        this.oldPrice = oldPrice;
    }

    public String getNewPrice() {
        return newPrice;
    }

    public void setNewPrice(String newPrice) {
        this.newPrice = newPrice;
    }

    public int getGiamGia() {
        return giamGia;
    }

    public void setGiamGia(int giamGia) {
        this.giamGia = giamGia;
    }

    public String getThuongHieu() {
        return thuongHieu;
    }

    public void setThuongHieu(String thuongHieu) {
        this.thuongHieu = thuongHieu;
    }


    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }


    public int getSoluongDH() {
        return soluongDH;
    }

    public void setSoluongDH(int soluongDH) {
        this.soluongDH = soluongDH;
    }

    public String getPriceDH() {
        return priceDH;
    }

    public void setPriceDH(String priceDH) {
        this.priceDH = priceDH;
    }

    
    
    
    
}
