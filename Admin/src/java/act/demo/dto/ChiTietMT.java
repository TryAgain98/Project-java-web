package act.demo.dto;

/**
 *
 * @author Toan
 */
public class ChiTietMT {
    private int maCT;
    private String ram;
    private String rom;
    private String cpu;
    private String heDH;
    private String manHinh;
    private String cardDH;
    private int maSP;

    public ChiTietMT() {
    }

    public int getMaCT() {
        return maCT;
    }

    public void setMaCT(int maCT) {
        this.maCT = maCT;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getRom() {
        return rom;
    }

    public void setRom(String rom) {
        this.rom = rom;
    }

   
    public String getCpu() {
        return cpu;
    }

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    public String getHeDH() {
        return heDH;
    }

    public void setHeDH(String heDH) {
        this.heDH = heDH;
    }

    public String getManHinh() {
        return manHinh;
    }

    public void setManHinh(String manHinh) {
        this.manHinh = manHinh;
    }

    public String getCardDH() {
        return cardDH;
    }

    public void setCardDH(String cardDH) {
        this.cardDH = cardDH;
    }

    public int getMaSP() {
        return maSP;
    }

    public void setMaSP(int maSP) {
        this.maSP = maSP;
    }
    
    
}
